#!/bin/bash

if [ ! $1 ]; then
       $1='dnmp'
fi

echo "**********************************************************************************************************************"
echo "强制删除已有$1容器"
echo "**********************************************************************************************************************"
docker rm -f $1

echo "**********************************************************************************************************************"
echo "强制删除已有$1镜像"
echo "**********************************************************************************************************************"
docker rmi -f $1

echo "**********************************************************************************************************************"
echo "创建$1镜像"
echo "**********************************************************************************************************************"
docker build -t $1 .


if [ ! -d ../$1/nginx ] ; then
  echo "**********************************************************************************************************************"
  echo "nginx配置目录不存在，创建并复制默认配置文件..."
  echo "**********************************************************************************************************************"
  mkdir ../$1/nginx
  cp conf/default.conf ../$1/nginx/default.conf
fi


if [ ! -d ../$1/www ] ; then
  echo "**********************************************************************************************************************"
  echo "www目录不存在，创建并复制默认站点主页文件..."
  echo "**********************************************************************************************************************"
  mkdir -p ../$1/www/default
  cp conf/index.php ../$1/www/default/
fi


if [ ! -d ../$1/logs ] ; then
  echo "**********************************************************************************************************************"
  echo "logs目录不存在，创建..."
  echo "**********************************************************************************************************************"
  mkdir ../$1/logs
fi


if [ ! -d ../$1/mysql ] ; then
  echo "**********************************************************************************************************************"
  echo "mysql目录不存，就创建..."
  echo "**********************************************************************************************************************"
  mkdir ../$1/mysql
fi

echo "**********************************************************************************************************************"
echo "根据镜像创建容器，运行并挂载目录"
echo "**********************************************************************************************************************"
if [ ! $2 ]; then  
       $2='80'  
fi  
docker run -d \
  -p 80:$2 \
  --name $1 \
  -v $PWD/../$1/nginx:/etc/nginx/conf.d \
  -v $PWD/../$1/mysql:/var/lib/mysql \
  -v $PWD/../$1/logs:/web/logs \
  -v $PWD/../$1/www:/web/www \
  $1

echo "**********************************************************************************************************************"
echo "显示当前的镜像列表"
echo "**********************************************************************************************************************"
docker images

echo "**********************************************************************************************************************"
echo "显示当前容器的进程"
echo "**********************************************************************************************************************"
docker ps

echo "**********************************************************************************************************************"
echo "输出$1容器的日志"
echo "**********************************************************************************************************************"
docker logs $1
